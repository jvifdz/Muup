    private void crearHistorialParcela(Animal animal, Parcela parcela, Date fechaEntrada) {

        Historialparcela historial = new Historialparcela();
        historial.setFkAnimal(animal);
        historial.setFkParcela(parcela);
        historial.setFechaEntrada(fechaEntrada);
        historial.setFechaSalida(null);
        historialController.create(historial);
        animal.getHistorialparcelaList().add(historial);

    }

