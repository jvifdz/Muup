    private void BtConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtConfirmarActionPerformed
        // TODO add your handling code here:

        if(this.jTableParcela.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null, "Seleccione una parcela", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        
        Date fechaEntrada;
        if (this.jDateFechaEntrada.getDate() == null) {
            fechaEntrada = new Date();
        } else {
            fechaEntrada = jDateFechaEntrada.getDate();
        }

        parcela = this.listaParcela.get(this.jTableParcela.getSelectedRow());

        for (int i = 0; i < this.animal.length; i++) {
            int numeroParcela = animal[i].getHistorialparcelaList().size() - 1;
            try {
                if (animal[i].getHistorialparcelaList().isEmpty()) {

                    crearHistorialParcela(animal[i], parcela, fechaEntrada);

                } else {

                    animal[i].getHistorialparcelaList().get(numeroParcela).setFechaSalida(fechaEntrada);
                    historialController.edit(animal[i].getHistorialparcelaList().get(numeroParcela));

                    crearHistorialParcela(animal[i], parcela, fechaEntrada);
                }

            } catch (Exception ex) {
                Logger.getLogger(FormAsignarParcela.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        creado = true;
        this.dispose();

    }//GEN-LAST:event_BtConfirmarActionPerformed

