    private void getTablaTotalFactura() {
        listaFactura.clear();
        List<Factura> lista;
        FacturaJpaController controlador = new FacturaJpaController();
        try {
            lista = controlador.FacturasCliente(this.cliente.getNombre());
            //lista = controlador.findAnimalEntities();
            for (int i = 0; i < lista.size(); i++) {

                FacturaAModelo(lista.get(i));
            }
            this.jTableFacturaClientes.setFocusable(false);
            this.jTableFacturaClientes.setModel(modeloFactura);
            this.LblTotal.setText("Total " + this.jTableFacturaClientes.getRowCount());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar los datos: " + e.toString());
        }

    }
    public void FacturaAModelo(Factura factura) {
        Object[] objeto = new Object[5];
        objeto[0] = factura.getIdFactura();
        objeto[1] = factura.getPrecioTotal();
        objeto[2] = factura.getIva();
        objeto[3] = new SimpleDateFormat("dd MMM yyyy").format(new Date(factura.getFechaVenta().getTime()));
        objeto[4] = factura.getFkdatosNegocio().getNombre();
        modeloFactura.addRow(objeto);
        listaFactura.add(factura);
    }
    

