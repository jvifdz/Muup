    private void ConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarActionPerformed
        // TODO add your handling code here:
        if(this.TxNombre.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Introduzca el nombre del cliente", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(this.TxDni.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Introduzca el dni del cliente", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(this.TxCodExplotacion.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Introduzca el código de explotacion del cliente", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(this.TxCodPostal.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Introduzca el código postal del cliente", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(this.TxNumeroTelf.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Introduzca el número de teléfono del cliente", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(this.TxDireccion.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Introduzca la dirección del cliente", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(this.TxCorreo.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Introduzca el correo del cliente", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        

        String nombre = this.TxNombre.getText();
        String DNI = this.TxDni.getText();
        String codExplotacion = this.TxCodExplotacion.getText();
        int codPostal = Integer.parseInt(this.TxCodPostal.getText());
        int numTelf = Integer.parseInt(this.TxNumeroTelf.getText());
        String direccion = this.TxDireccion.getText();
        String correo = this.TxCorreo.getText();

        cliente.setNombre(nombre);
        cliente.setDni(DNI);
        cliente.setCodExplotacion(codExplotacion);
        cliente.setCodPostal(codPostal);
        cliente.setDireccion(direccion);
        cliente.setTelefono(numTelf);
        cliente.setCorreo(correo);
        
        creado = true;
        this.dispose();
        
    }//GEN-LAST:event_ConfirmarActionPerformed

